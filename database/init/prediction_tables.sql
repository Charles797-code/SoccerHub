CREATE SEQUENCE SEQ_MATCH_PREDICTION START WITH 1 INCREMENT BY 1 NOMAXVALUE NOCYCLE;

ALTER TABLE SYS_USER ADD POINTS NUMBER DEFAULT 1000;

COMMENT ON COLUMN SYS_USER.POINTS IS '用户积分';

CREATE TABLE MATCH_PREDICTION (
    PREDICTION_ID       NUMBER          DEFAULT SEQ_MATCH_PREDICTION.NEXTVAL PRIMARY KEY,
    USER_ID             NUMBER          NOT NULL REFERENCES SYS_USER(USER_ID),
    MATCH_ID            VARCHAR2(50)    NOT NULL REFERENCES MATCH_SCHEDULE(MATCH_ID),
    PREDICTED_RESULT    VARCHAR2(20)    NOT NULL CHECK (PREDICTED_RESULT IN ('HOME_WIN', 'AWAY_WIN', 'DRAW')),
    PREDICTED_HOME_SCORE NUMBER,
    PREDICTED_AWAY_SCORE NUMBER,
    STATUS              VARCHAR2(20)    DEFAULT 'PENDING' CHECK (STATUS IN ('PENDING', 'SETTLED')),
    POINTS_EARNED       NUMBER          DEFAULT 0,
    CREATED_AT          TIMESTAMP       DEFAULT SYSTIMESTAMP,
    SETTLED_AT          TIMESTAMP,
    UNIQUE (USER_ID, MATCH_ID)
);

COMMENT ON TABLE MATCH_PREDICTION IS '比赛预测表 - 用户对比赛结果的预测';
COMMENT ON COLUMN MATCH_PREDICTION.PREDICTED_RESULT IS '预测结果: HOME_WIN=主队胜, AWAY_WIN=客队胜, DRAW=平局';
COMMENT ON COLUMN MATCH_PREDICTION.STATUS IS '状态: PENDING=待开奖, SETTLED=已结算';
COMMENT ON COLUMN MATCH_PREDICTION.POINTS_EARNED IS '获得的积分';

CREATE INDEX IDX_PREDICTION_USER ON MATCH_PREDICTION(USER_ID);
CREATE INDEX IDX_PREDICTION_MATCH ON MATCH_PREDICTION(MATCH_ID);
CREATE INDEX IDX_PREDICTION_STATUS ON MATCH_PREDICTION(STATUS);

UPDATE SYS_USER SET POINTS = 1000 WHERE POINTS IS NULL;