            CASE ROW_NUMBER() OVER (ORDER BY DBMS_RANDOM.VALUE)
                WHEN 1 THEN 8
                WHEN 2 THEN 6
                WHEN 3 THEN 4
                WHEN 4 THEN 3
                ELSE 2
            END
        WHEN POSITION = 'DF' THEN 
            CASE ROW_NUMBER() OVER (ORDER BY DBMS_RANDOM.VALUE)
                WHEN 1 THEN 3
                WHEN 2 THEN 2
                ELSE 1
            END
        ELSE 0
    END,
    CASE 
        WHEN POSITION = 'FW' THEN FLOOR(DBMS_RANDOM.VALUE(5, 12))
        WHEN POSITION = 'MID' THEN FLOOR(DBMS_RANDOM.VALUE(3, 8))
        ELSE FLOOR(DBMS_RANDOM.VALUE(0, 3))
    END,
    CASE 
        WHEN POSITION = 'DF' THEN FLOOR(DBMS_RANDOM.VALUE(3, 6))
        WHEN POSITION = 'MID' THEN FLOOR(DBMS_RANDOM.VALUE(2, 5))
        ELSE FLOOR(DBMS_RANDOM.VALUE(0, 3))
    END,
    FLOOR(DBMS_RANDOM.VALUE(0, 2)),
    CASE WHEN POSITION = 'GK' THEN 30 ELSE FLOOR(DBMS_RANDOM.VALUE(15, 32)) END,
    CASE WHEN POSITION = 'GK' THEN 2700 ELSE FLOOR(DBMS_RANDOM.VALUE(800, 2500)) END
