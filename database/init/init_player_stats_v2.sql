    CASE 
        WHEN POSITION = 'FW' THEN 
            CASE ROW_NUMBER() OVER (ORDER BY DBMS_RANDOM.VALUE)
                WHEN 1 THEN 18
                WHEN 2 THEN 12
                WHEN 3 THEN 5
                ELSE 1
            END
