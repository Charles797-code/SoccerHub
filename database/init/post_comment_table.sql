-- 帖子评论表
CREATE TABLE POST_COMMENT (
    COMMENT_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    POST_ID NUMBER NOT NULL,
    USER_ID NUMBER NOT NULL,
    PARENT_ID NUMBER,
    CONTENT VARCHAR2(2000) NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    UPDATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP,
    IS_DELETED NUMBER(1) DEFAULT 0,
    CONSTRAINT FK_COMMENT_POST FOREIGN KEY (POST_ID) REFERENCES POST(POST_ID) ON DELETE CASCADE,
    CONSTRAINT FK_COMMENT_USER FOREIGN KEY (USER_ID) REFERENCES SYS_USER(USER_ID) ON DELETE CASCADE,
    CONSTRAINT FK_COMMENT_PARENT FOREIGN KEY (PARENT_ID) REFERENCES POST_COMMENT(COMMENT_ID) ON DELETE SET NULL
);

-- 创建索引
CREATE INDEX IDX_COMMENT_POST ON POST_COMMENT(POST_ID);
CREATE INDEX IDX_COMMENT_USER ON POST_COMMENT(USER_ID);
CREATE INDEX IDX_COMMENT_PARENT ON POST_COMMENT(PARENT_ID);

-- 添加评论
COMMENT ON TABLE POST_COMMENT IS '帖子评论表';
COMMENT ON COLUMN POST_COMMENT.COMMENT_ID IS '评论ID';
COMMENT ON COLUMN POST_COMMENT.POST_ID IS '帖子ID';
COMMENT ON COLUMN POST_COMMENT.USER_ID IS '评论用户ID';
COMMENT ON COLUMN POST_COMMENT.PARENT_ID IS '父评论ID（用于回复评论）';
COMMENT ON COLUMN POST_COMMENT.CONTENT IS '评论内容';
COMMENT ON COLUMN POST_COMMENT.CREATED_AT IS '创建时间';
COMMENT ON COLUMN POST_COMMENT.UPDATED_AT IS '更新时间';
COMMENT ON COLUMN POST_COMMENT.IS_DELETED IS '是否删除';
